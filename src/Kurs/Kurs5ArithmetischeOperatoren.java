package Kurs;

public class Kurs5ArithmetischeOperatoren {
	
	public static void main(String[] args) {
		
		int number1 = 3; 
		int number2 = 5; 
		System.out.println(number1 +  "" + number2); // Wenn man "" benutzt, verkettet man die Zahlen 3und5= 35
		
		System.out.println(number1+ + number2); // Ohne die Verkettung ("") werden sie in dem Fall addiert = 8
		
	}
}